開始 = int(input("請輸入開始整數:"))
# 使用者輸入結束整數
結束 = int(input("請輸入結束整數:"))

a = 開始  # 起始數字
while a <= 結束:
    # 只檢查大於等於 2 的數，因為 2 是最小的質數
    if a >= 2:
        i = 2  # 檢查是否能整除的除數從 2 開始
        是不是質數 = False  # 記錄這個數是否能被整除（如果能，就不是質數）

        # 當 i*i 小於等於 a 時繼續檢查
        while i * i <= a:
            if a % i == 0:  # 如果 a 可以被 i 整除
                是不是質數 = True  # 表示不是質數
            i += 1  # 檢查下一個 i

        # 如果沒有被整除過，代表是質數，就印出來
        if 是不是質數 == False:
            print(a)

    # 檢查下一個數字
    a += 1
