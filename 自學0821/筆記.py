# 參數化格式（%）

# %5d
# 介紹：格式化整數（d = decimal），欄位寬度是 5 格。
# 原因：為了讓數字對齊，會自動在前面補空格。
# 用途：印表格或數字時，讓輸出整齊。
# 舉例：
print("%5d" % 7)  # "    7"   (前面有 4 個空格)
print("%5d" % 12345)  # "12345"   (剛好 5 格，沒有空格)

# %5s
# 介紹：格式化字串（s = string），欄位寬度是 5 格。
# 原因：為了讓文字對齊，不夠長會自動在前面補空格。
# 用途：排版文字表格。
# 舉例：
print("%5s" % "hi")  # "   hi"  (前面有 3 個空格)
print("%5s" % "hello")  # "hello" (剛好 5 格，沒有空格)

# %8.2f
# 介紹：格式化浮點數（f = float），欄位寬度是 8 格，小數點後 2 位。
# 原因：讓數字有固定長度，並控制小數精度。
# 用途：處理金額、科學數據的輸出。
# 舉例：
print("%8.2f" % 12.3)  # "   12.30" (前面 3 空格，總共 8 格)
print("%8.2f" % 1234.56)  # " 1234.56" (前面 1 空格)
# 解釋：
# %5d –– 負責處理整數
# %5s –– 負責處理字串
# %8.2f –– 負責處理浮點數
# 裡面的數字都可以改（%5d可以改成%10d）
# 裡面的字串都可以改（%5s可以改成%10s）
# 裡面的浮點數都可以改（%8.2f可以改成%10.3f）
